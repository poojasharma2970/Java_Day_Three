package HandsOn;
import java.util.*;

public class ReplaceCharacter {

	public static void main(String[] args) {
		String str = "I love fogs";
		
		System.out.println("The original string is : \n"+str);
		System.out.println("\nThe New string is : ");
		System.out.println(str.replace('f', 'D'));
	}

}
